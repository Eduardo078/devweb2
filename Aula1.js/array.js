//estudando arrays e suas funções em js; EM JAVASCRIPT o array é basicamente uma lista, mas em outras linguagens são coisas diferentes.

let frutas = ["Maçã", "Banana", "Laranja", "Goiaba"];

frutas.sort()
console.log(frutas[0])
console.log(frutas[1])

//Adiciona um elemento no final
frutas.push("Uva");

//Adiciona um elemento no início
frutas.unshift("Melancia");

//remove o último elemento
frutas.pop();

//Remove o primeiro elemento
frutas.shift();

console.log(frutas);

